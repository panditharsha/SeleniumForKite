package practise;

public class Switch {

	public static void main(String[] args) {
		int mark =5;
		
	switch(mark) 
	{
	case 1 :
		System.out.println("Number one ");
		break;
	case 2 :
		System.out.println("Number two ");
		break;
	case 3:
		System.out.println("Number three ");
		break;
	case 4 :
		System.out.println("Number four ");
		break;
		
	default:
		System.out.println("please enter valid number betwn 1-4");
	    break;
	}
	
	
	

	}

}
